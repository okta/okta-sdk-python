def create_dpop_client(dpop_config, fs):
    """
    Helper to create DPoP-enabled OktaClient with filesystem handling.

    Pauses fake filesystem during client creation to allow Cryptodome
    native modules to load properly.
    """
    fs.pause()
    client = OktaClient(dpop_config)
    fs.resume()
    return client


