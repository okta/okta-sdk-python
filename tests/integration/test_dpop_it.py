# Helper functions for manual testing
async def create_dpop_test_app(org_url: str, api_token: str) -> Dict[str, Any]:
    """
    Helper function to create a DPoP-enabled OIDC application.
    Can be used for manual testing.

    Args:
        org_url: Okta org URL
        api_token: API token for authentication

    Returns:
        Dict with application details including client_id
    """
    client = OktaClient({
        'orgUrl': org_url,
        'token': api_token
    })

    app_label = f"DPoP_Test_App_{uuid.uuid4().hex[:8]}"

    oidc_settings_client = models.OpenIdConnectApplicationSettingsClient(
        grant_types=[models.GrantType.CLIENT_CREDENTIALS],
        application_type=models.OpenIdConnectApplicationType.SERVICE,
        dpop_bound_access_tokens=True,
        token_endpoint_auth_method=models.OAuthEndpointAuthenticationMethod.PRIVATE_KEY_JWT
    )

    oidc_settings = models.OpenIdConnectApplicationSettings(
        oauthClient=oidc_settings_client
    )

    oidc_app = models.OpenIdConnectApplication(
        label=app_label,
        sign_on_mode=models.ApplicationSignOnMode.OPENID_CONNECT,
        settings=oidc_settings
    )

    created_app, _, err = await client.create_application(oidc_app)
    if err:
        raise Exception(f"Failed to create app: {err}")

    return {
        'id': created_app.id,
        'label': created_app.label,
        'client_id': created_app.credentials.o_auth_client.client_id,
    }


